<template>
  <div id="app">
    <ul>
      <li v-for="(item, index) in myArr" :key="index">{{ item }}</li>
    </ul>
    <button @click="btn">走一走</button>
  </div>
</template>

<script>
export default {
  data() {
    return {
      myArr: ['帅哥', '美女', '程序猿'],
    }
  },
  methods: {
    btn() {
      this.myArr.push(this.myArr.shift())
    },
  },
}
</script>
